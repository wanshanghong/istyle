package com.istyle.pojo;

/**
 * @author 黄文伟
 */
public class TbStylist {
    private Long stylistId;
    private String stylistName;
    private String stylistPhoto;
    private String stylistWord;
    private String realName;
    private String stylistPassword;
    private String stylistSex;
    private int stylistAge;
    private String stylistPhone;
    private long stylistAdvisory;
    private String stylistIntroduction;
	public Long getStylistId() {
		return stylistId;
	}
	public void setStylistId(Long stylistId) {
		this.stylistId = stylistId;
	}
	public String getStylistName() {
		return stylistName;
	}
	public void setStylistName(String stylistName) {
		this.stylistName = stylistName;
	}
	public String getStylistPhoto() {
		return stylistPhoto;
	}
	public void setStylistPhoto(String stylistPhoto) {
		this.stylistPhoto = stylistPhoto;
	}
	public String getStylistWord() {
		return stylistWord;
	}
	public void setStylistWord(String stylistWord) {
		this.stylistWord = stylistWord;
	}
	public String getRealName() {
		return realName;
	}
	public void setRealName(String realName) {
		this.realName = realName;
	}
	public String getStylistPassword() {
		return stylistPassword;
	}
	public void setStylistPassword(String stylistPassword) {
		this.stylistPassword = stylistPassword;
	}
	public String getStylistSex() {
		return stylistSex;
	}
	public void setStylistSex(String stylistSex) {
		this.stylistSex = stylistSex;
	}
	public int getStylistAge() {
		return stylistAge;
	}
	public void setStylistAge(int stylistAge) {
		this.stylistAge = stylistAge;
	}
	public String getStylistPhone() {
		return stylistPhone;
	}
	public void setStylistPhone(String stylistPhone) {
		this.stylistPhone = stylistPhone;
	}
	public long getStylistAdvisory() {
		return stylistAdvisory;
	}
	public void setStylistAdvisory(long stylistAdvisory) {
		this.stylistAdvisory = stylistAdvisory;
	}
	public String getStylistIntroduction() {
		return stylistIntroduction;
	}
	public void setStylistIntroduction(String stylistIntroduction) {
		this.stylistIntroduction = stylistIntroduction;
	}
	@Override
	public String toString() {
		return "TbStylist [stylistId=" + stylistId + ", stylistName=" + stylistName + ", stylistPhoto=" + stylistPhoto
				+ ", stylistWord=" + stylistWord + ", realName=" + realName + ", stylistPassword=" + stylistPassword
				+ ", stylistSex=" + stylistSex + ", stylistAge=" + stylistAge + ", stylistPhone=" + stylistPhone
				+ ", stylistAdvisory=" + stylistAdvisory + ", stylistIntroduction=" + stylistIntroduction + "]";
	}
    
}
